void setTextCoords ( int width, int height )
{
  glMatrixMode( GL_PROJECTION );
  glPushMatrix();
  glLoadIdentity();
  gluOrtho2D( 0.0f, SINGLE(width), 0.0f, SINGLE(height) );
  glMatrixMode( GL_MODELVIEW );
  glPushMatrix();
  glLoadIdentity();
  glPixelStorei( GL_UNPACK_ROW_LENGTH,  1 );
  glPixelStorei( GL_UNPACK_ALIGNMENT,   1 );
  glPixelStorei( GL_UNPACK_SKIP_PIXELS, 0 );
  glPixelStorei( GL_UNPACK_SKIP_ROWS,   0 );
  glPushAttrib( GL_LIGHTING_BIT );
  glDisable( GL_LIGHTING );
}

void setTextPosition ( int x, int y )
{
  glRasterPos3i( x, y, 1 );
}

void unsetTextCoords ()
{
  glPopAttrib();
  glMatrixMode( GL_PROJECTION );
  glPopMatrix();
  glMatrixMode( GL_MODELVIEW );
  glPopMatrix();
}

void drawText ( const char *str, int fontNumber )
{
  unsigned columns = 0;
  const unsigned8 *fptr;
  unsigned8 high;

  switch ( fontNumber )
  {
  case 1:
    fptr = fontB8x16;
    high = (unsigned8)( sizeof(fontB8x16)/16 - 1 );
    break;
  default:
    fptr = fontA8x16;
    high = (unsigned8)( sizeof(fontA8x16)/16 - 1 );
  }

  while ( *str )
  {
    unsigned8 ch = (unsigned8)( *str++ );

    if ( ch == '\n' )
    {
      glBitmap( 0, 0, 0.0f, 0.0f, columns * -8.0f, -16.0f, fptr );
      columns = 0;
      continue;
    }

    if ( ch > high )
      ch = ' ';

    glBitmap( 8, 16, 0.0f, 0.0f, 8.0f, 0.0f, fptr + (((unsigned)ch) << 4) );
    columns++;
  }
}
